package res;

import java.sql.Date;

public class ResDTO {
	
	
	
	private String res_no; 
	private String res_purpose;
	private String res_request;
	private String res_people;
	private String res_date;
	
	public String getRes_no() {
		return res_no;
	}
	public void setRes_no(String res_no) {
		this.res_no = res_no;
	}
	public String getRes_purpose() {
		return res_purpose;
	}
	public void setRes_purpose(String res_purpose) {
		this.res_purpose = res_purpose;
	}
	public String getRes_request() {
		return res_request;
	}
	public void setRes_request(String res_request) {
		this.res_request = res_request;
	}
	public String getRes_people() {
		return res_people;
	}
	public void setRes_people(String res_people) {
		this.res_people = res_people;
	}
	public String getRes_date() {
		return res_date;
	}
	public void setRes_date(String res_date) {
		this.res_date = res_date;
	}
	
	
	
}

